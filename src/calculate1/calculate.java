package calculate1;

public class calculate {
	 public static int add(int x, int y)
	    {
	        int result = x + y;
	        return result;
	    }
	    public static int sub(int x, int y)
	    {
	        int result = x-y;
	        return result;
	    }
	    public static int mult(int x, int y)
	    {
	        int result = x*y;
	        return result;
	    }
	   

}
